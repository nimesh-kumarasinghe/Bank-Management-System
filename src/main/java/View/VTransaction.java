    private void btn_addInterestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addInterestActionPerformed
        // TODO add your handling code here:
        CTransaction ctransac=new CTransaction();

        if(txt_interest == null || txt_interest.getText().trim().isEmpty())
        {   JOptionPane.showMessageDialog(null,"Enter Interest","Error",0);
        }
        else if(cmb_accountType.getSelectedIndex() == 0)
        {   JOptionPane.showMessageDialog(null,"Select the Account Type","Error",0);
        }
        else if(!txt_interest.getText().matches("[0-9].[0-9]+"))
        {   JOptionPane.showMessageDialog(null, "Enter Numeric Value Only","Error",0);
        }
        else
        {
            float interest=Float.parseFloat(txt_interest.getText());
            if (cmb_accountType.getSelectedIndex() == 1) //Childrens Savings Account
            {   String account_type="Childrens Saving Account";
                ctransac.addInterest(account_type,interest );
            }
            else if (cmb_accountType.getSelectedIndex() == 2) //Youth Savings Account
            {   String account_type="Youth Saving Account";
                ctransac.addInterest(account_type, interest);
            }
            else if (cmb_accountType.getSelectedIndex() == 3) //Senior Citizen Savings Account
            {   String account_type="Senior Citizen Saving Account";
                ctransac.addInterest(account_type, interest);
            }
        }
    }//GEN-LAST:event_btn_addInterestActionPerformed

