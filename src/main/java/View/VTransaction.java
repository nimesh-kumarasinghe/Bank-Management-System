    private void btn_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SearchActionPerformed
        // TODO add your handling code here:
        if(txt_AccountIdSearch == null || (txt_AccountIdSearch.getText()).trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Enter Account Number","Information",0);
        }
        else
        {
            try
            {
                String account_id=txt_AccountIdSearch.getText();
                ResultSet rs;
                CTransaction ctransac=new CTransaction();
                rs=ctransac.getAccountDetails(account_id);
                /*if(rs.next() == false) {
                    JOptionPane.showMessageDialog(null,"Account Number Does not exist","Error",0);
                }*/
                if(rs.next()) {
                    String AccID = rs.getString("AccountID");
                    txt_AccountNumber.setText(AccID);
                    String CusID = rs.getString("CusID");
                    txt_CustomerId.setText(CusID);
                    String AccType = rs.getString("AccType");
                    txt_AccountType.setText(AccType);
                    Float AccBalance = rs.getFloat("AccountBalance");
                    txt_AccountBalance.setText(String.valueOf(AccBalance));
                }
            }
            catch(SQLException e)
            {   JOptionPane.showMessageDialog(null,e.getMessage());
            }
            catch(Exception e)
            {   JOptionPane.showMessageDialog(null,e.getMessage());
            }
        }
    }//GEN-LAST:event_btn_SearchActionPerformed

    private void btn_depositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_depositActionPerformed
        // TODO add your handling code here:
        if(txt_depositAmount == null || (txt_depositAmount.getText()).trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Enter Deposit Amount","Information",0);
        }
        else if(!txt_depositAmount.getText().matches("[0-9]+"))
        {
            JOptionPane.showMessageDialog(null,"Enter Numeric Values Only","Information",0);
        }
        else
        {
            try {
                ResultSet rsAmount;
                String account_id=txt_AccountNumber.getText();
                float deposit_amount=Float.parseFloat(txt_depositAmount.getText());
                CTransaction ctransac=new CTransaction();
                rsAmount=ctransac.depositMoney(account_id,deposit_amount);
                if(rsAmount.next()) {
                    Float AccBalance = rsAmount.getFloat("AccountBalance");
                    txt_AccountBalance.setText(String.valueOf(AccBalance));
                }
                txt_depositAmount.setText("");
            }
            catch(SQLException e)
            {   JOptionPane.showMessageDialog(null,e.getMessage());
            }
            catch(Exception e)
            {   JOptionPane.showMessageDialog(null,e.getMessage());
            }
        }
    }//GEN-LAST:event_btn_depositActionPerformed

    private void btn_withdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_withdrawActionPerformed
        // TODO add your handling code here:
        if(txt_withdrawAmount == null || (txt_withdrawAmount.getText()).trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Enter Withdraw Amount","Error",0);
        }
        else if(!txt_withdrawAmount.getText().matches("[0-9]+"))
        {
            JOptionPane.showMessageDialog(null,"Enter Numeric Values Only","Error",0);
        }
        else if(Float.parseFloat(txt_withdrawAmount.getText())>Float.parseFloat(txt_AccountBalance.getText()))
        {   JOptionPane.showMessageDialog(null,"Insufficient Funds","Error",0);
        }
        else
        {
            try {
                ResultSet rsDAmount;
                String account_id=txt_AccountNumber.getText();
                float withdraw_amount=Float.parseFloat(txt_withdrawAmount.getText());
                CTransaction ctransac=new CTransaction();
                rsDAmount=ctransac.withdrawMoney(account_id, withdraw_amount);
                if(rsDAmount.next()) {
                    Float AccBalance=rsDAmount.getFloat("AccountBalance");
                    txt_AccountBalance.setText(String.valueOf(AccBalance));
                }
                txt_withdrawAmount.setText("");
            }
            catch (SQLException e)
            {   JOptionPane.showMessageDialog(null,e.getMessage());
            }
            catch(Exception e)
            {   JOptionPane.showMessageDialog(null,e.getMessage());
            }
        }
    }//GEN-LAST:event_btn_withdrawActionPerformed

