    private void btn_add_staffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_staffActionPerformed
        if(text_fname.getText().equals(""))
        {   JOptionPane.showMessageDialog(null,"Full Name cannot be blank");
        }
        else if(text_name_with_i.getText().equals(""))
        {   JOptionPane.showMessageDialog(null,"Name With Initials cannot be blank");
        }
        else if(text_fname.getText().matches("[0-9]+") || text_name_with_i.getText().matches("[0-9]+"))
        {   JOptionPane.showMessageDialog(null,"Full Name & Name With Initials cannot have numbers");
        }
        else if(dob_picker.getDateEditor().getDate() == null)
        {   JOptionPane.showMessageDialog(null,"Date of birth cannot be blank");
        }
        else if(text_age.getText().equals(""))
        {   JOptionPane.showMessageDialog(null,"Age cannot be blank");
        }
        else if(!text_age.getText().matches("[0-9]+"))
        {   JOptionPane.showMessageDialog(null,"Age should be numeric only");
        }
        else if(text_nic.getText().equals(""))
        {   JOptionPane.showMessageDialog(null,"NIC cannot be blank");
        }
        else if(text_address.getText().equals(""))
        {   JOptionPane.showMessageDialog(null,"Address cannot be blank");
        }
        else if(text_t_no.getText().equals(""))
        {   JOptionPane.showMessageDialog(null,"Phone number cannot be blank");
        }
        else if(!text_t_no.getText().matches("[0-9]+"))
        {   JOptionPane.showMessageDialog(null,"Phone number should be numeric only");
        }
        else if(text_t_no.getText().length()<10 || text_t_no.getText().length()>10)
        {   JOptionPane.showMessageDialog(null,"Phone number should have 10 numbers");
        }
        else if(text_email.getText().equals(""))
        {   JOptionPane.showMessageDialog(null,"Please Enter Email Address");
        }
        else if(!emailValidator(text_email.getText())==true)
        {   JOptionPane.showMessageDialog(null,"Enter a valid email. Ex:name@gmail.com");
        }
        else if(text_password.getText().equals(""))
        {   JOptionPane.showMessageDialog(null,"Please Enter A Password");
        }
        else if(text_password.getText().length()<8)
        {   JOptionPane.showMessageDialog(null,"Please Enter Password Longer Than 8 Characters");
        }
        else
        {
            String user_type="";
            String id,fname,name_with_i,age,nic,address,t_no,email,password;
            int selected_index_type = type_selector.getSelectedIndex();

            if(selected_index_type == 0)
            {
                user_type = "admin";
            }
            else if(selected_index_type == 1)
            {   user_type = "staff";
            }

            String gender = (String) gender_type_selector.getSelectedItem();
            id = text_id.getText();
            fname = text_fname.getText();
            name_with_i = text_name_with_i.getText();

            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
            Date date = dob_picker.getDate();
            String dob = dateFormat.format(date);

            age = text_age.getText();
            nic = text_nic.getText();
            address = text_address.getText();
            t_no = text_t_no.getText();
            email = text_email.getText();
            password = text_password.getText();

            CAddUser userdata = new CAddUser();
            userdata.saveUserData(user_type, id, fname, name_with_i, dob, age, nic, gender, address, t_no, email, password);
        }
    }//GEN-LAST:event_btn_add_staffActionPerformed

